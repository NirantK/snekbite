@snek_types.register("fancy")
def fancy():
    return fancy_snek

def get_sneks(snek_type):
    print("All loaders:", snek_types.get_all())
    loader = snek_types.get(snek_type)
    print("Entry Points", snek_types.get_entry_points())
    return loader